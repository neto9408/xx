public class Equipo {
    private int id_Equipo; 
    private String nombre_Equipo;
    private String referencia_Equipo;
    private int cantidad_Equipo;
    private String programa_Equipo;
    private String modelo_Equipo;

     public int getId_Equipo() {
        return id_Equipo;
    }

    public void setId_Equipo(int id_Equipo) {
        this.id_Equipo = id_Equipo;
    }

    public String getNombre_Equipo() {
        return nombre_Equipo;
    }

    public void setNombre_Equipo(String nombre_Equipo) {
        this.nombre_Equipo = nombre_Equipo;
    }

    public String getReferencia_Equipo() {
        return referencia_Equipo;
    }

    public void setReferencia_Equipo(String referencia_Equipo) {
        this.referencia_Equipo = referencia_Equipo;
    }

    public int getCantidad_Equipo() {
        return cantidad_Equipo;
    }

    public void setCantidad_Equipo(int cantidad_Equipo) {
        this.cantidad_Equipo = cantidad_Equipo;
    }

    public String getPrograma_Equipo() {
        return programa_Equipo;
    }

    public void setPrograma_Equipo(String programa_Equipo) {
        this.programa_Equipo = programa_Equipo;
    }

    public String getModelo_Equipo() {
        return modelo_Equipo;
    }

    public void setModelo_Equipo(String modelo_Equipo) {
        this.modelo_Equipo = modelo_Equipo;
    }
    
    
}
